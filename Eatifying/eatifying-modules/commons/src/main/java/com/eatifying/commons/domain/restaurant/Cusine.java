package com.eatifying.commons.domain.restaurant;

/**
 * Class to represent Cusine types
 * 
 * @author Dhass
 * 
 */
public class Cusine {
	int cusineId;
	String cusineName;
	String cusineDesc;

	public Cusine() {

	}

	public int getCusineId() {
		return cusineId;
	}

	public void setCusineId(int cusineId) {
		this.cusineId = cusineId;
	}

	public String getCusineName() {
		return cusineName;
	}

	public void setCusineName(String cusineName) {
		this.cusineName = cusineName;
	}

	public String getCusineDesc() {
		return cusineDesc;
	}

	public void setCusineDesc(String cusineDesc) {
		this.cusineDesc = cusineDesc;
	}

}
